# Type conversions
typecon = [
    int(3.2),
    float(4),
    str(645),
]
print(typecon)

# Get the type of something
print(type("Hello there!"))
print(type(52))
print(type(5.2))

# Basic functions
print(abs(-345))

# 2nd argument of round() is the # of digits you want to round to
print(round(27.3658, 2))
